void intToStr(int num, char *str) {
int i = 0;
int isNeg = 0;
if (num == 0) {
str[i++] = '0';
str[i] = '\0';
return;
}
if (num < 0) {
isNeg = 1;
num = -num;
}
while (num != 0) {
str[i++] = (num % 10) + '0';
num /= 10;
}
if (isNeg)
str[i++] = '-';
str[i] = '\0';
// Reverse the string
for (int j = 0; j < i / 2; j++) {
char t = str[j];
str[j] = str[i - j - 1];
str[i - j - 1] = t;
}
}
