#define TEMP_PIN GPIO_PIN_0
#define TEMP_PORT GPIOA

void DHT_Start(void) {
    HAL_GPIO_WritePin(TEMP_PORT, TEMP_PIN, GPIO_PIN_RESET);
    HAL_Delay(20);
    HAL_GPIO_WritePin(TEMP_PORT, TEMP_PIN, GPIO_PIN_SET);
}

uint8_t DHT_CheckResponse(void) {
    uint8_t response = 0;
    HAL_Delay(1);
    if(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_RESET) {
        response = 1;
    }
    while(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_RESET);
    while(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_SET);
    return response;
}

uint8_t DHT_ReadByte(void) {
    uint8_t i = 0, j;
    for(j=0;j<8;j++) {
        while(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_RESET);
        HAL_Delay(1);
        i <<= 1;
        if(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_SET)
            i |=1;
        while(HAL_GPIO_ReadPin(TEMP_PORT, TEMP_PIN) == GPIO_PIN_SET);
    }
    return i;
}

int8_t DHT_ReadTemp(void){
    uint8_t RH1,RH2,T1,T2,checksum;
    int8_t temperature = 0;

    DHT_Start();
    if(DHT_CheckResponse()){
        RH1 = DHT_ReadByte();
        RH2 = DHT_ReadByte();
        T1  = DHT_ReadByte();
        T2  = DHT_ReadByte();
        checksum = DHT_ReadByte();
        temperature = T1; //integer temp only
        (void)RH1;
		(void)RH2;
		(void)T2;
		(void)checksum;
    }
    return temperature;
}

